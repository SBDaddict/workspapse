package ch3;

public class Ch3_12 {

    public static void main(String[] args){

        int x = 10;
        int c = 8;
        System.out.printf("%d 와 %d의 나머지 값은 : %d 이다. %n",x, c, x % c);

        int x1 = 10;
        int c1 = -8;
        System.out.printf("%d 와 %d의 나머지 값은 : %d 이다. %n",x1, c1, x1 % c1);
    }
}
